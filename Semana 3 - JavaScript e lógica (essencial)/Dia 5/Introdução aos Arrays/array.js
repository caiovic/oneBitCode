/*
  Arrays
 É uma estrutura de dados que são organizados em formato
 de lista onde cada valor fica em uma determinada posição
 além de ser possível atribuir qualquer tipo de valor a uma
 determinada posição, só separar por virgulas. Além de poder
 armazenar dentro de um array outro array
*/

let tiposDeNaves = ['Supernova', 'Elemental', 'Helmet']

console.log(tiposDeNaves[0]) // "Supernova"

console.log(tiposDeNaves[1]) // "Elemental"

console.log(tiposDeNaves[2]) // "Helmet"

console.log(tiposDeNaves) // "["Supernova", "Elemental", "Helmet"]"
