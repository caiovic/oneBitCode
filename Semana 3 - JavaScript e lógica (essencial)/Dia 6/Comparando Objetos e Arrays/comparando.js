// objeto
let estacaoEspacial = {
  nome: 'Receptora',
  qntNaves: 15,
  formato: 'Redonda'
}

console.log(estacaoEspacial.nome.toUpperCase()) // RECEPTORA

// array

let carros = ['corsa', 'palio', 'celta']

console.log(carros[0].toUpperCase()) // CORSA

/*
Em resumo:

array: uma lista sem propriedades que pode ser acessada através de indices


object: uma lista, porém ordenada com propriedades e acessada através delas
*/
